"""Evaluation orchestration utilities."""

from typing import TypedDict

import numpy as np

from evaluation.metrics.accuracy import compute_accuracy


class EvaluationOutput(TypedDict):
    """Output values from model evaluation."""

    accuracy: float


def run_evaluation(
    y: np.ndarray,
    ypred: np.ndarray,
) -> EvaluationOutput:
    """Run model evaluation metrics.

    Args:
        y: True class labels.
        ypred: Predicted class labels generated by the model.

    Returns:
        Dictionary containing evaluation metric results.
    """
    accuracy = compute_accuracy(
        y=y,
        ypred=ypred,
    )

    return {
        "accuracy": accuracy["accuracy"],
    }
