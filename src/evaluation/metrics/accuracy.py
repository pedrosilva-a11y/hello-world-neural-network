"""Accuracy metric computation."""

import numpy as np


def compute_accuracy(y: np.ndarray, ypred: np.ndarray) -> dict[str, float]:
    """Compute accuracy between true labels and predicted labels.

    Args:
        y: True class labels.
        ypred: Predicted class labels generated by the forward pass.

    Returns:
        Dictionary containing the accuracy value.
    """
    return {
        "accuracy": float(np.mean(y == ypred)),
    }
